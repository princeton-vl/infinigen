from typing import NamedTuple

import numpy as np
import procfunc as pf
from procfunc.nodes import types as t

from infinigen_v2.generators.curves.room_edge import (
    RoomEdgeCurveResult,
    room_edge_curve_distribution,
)
from infinigen_v2.generators.util.curve import curve_to_mesh_with_uv
from infinigen_v2.generators.util.mesh import crease_sharp, grid_from_corners


class RoomShapeResult(NamedTuple):
    walls: pf.MeshObject
    flat_walls: list[pf.MeshObject]
    floor: pf.MeshObject
    ceiling: pf.MeshObject
    dimensions: pf.Vector
    edge_curve: RoomEdgeCurveResult

    @property
    def all_objects(self) -> list[pf.MeshObject]:
        return [self.walls, self.floor, self.ceiling]


class RoomEdgeToWallsResult(NamedTuple):
    geometry: pf.ProcNode[pf.MeshObject]
    vector: pf.ProcNode[pf.Vector]
    is_face_flat: pf.ProcNode[bool]


@pf.nodes.node_function
def room_edge_to_walls(
    curve: pf.ProcNode[pf.CurveObject],
    height: t.SocketOrVal[float],
    is_edge_point_flat: t.SocketOrVal[bool],
) -> RoomEdgeToWallsResult:
    # Code generated by procfunc v0.16.1

    curve_line_end = pf.nodes.math.vector_multiply_add(
        a=height.astype(dtype=pf.Vector),
        b=(0.0, -1.0, 0.0),
        addend=(0.0, -0.01, 0.0),
    )
    curve_line = pf.nodes.geo.curve_line(start=(0.0, 0.01, 0.0), end=curve_line_end)

    input_index = pf.nodes.geo.input_index()

    capture_attribute = pf.nodes.geo.capture_attribute(
        geometry=curve_line, index=input_index
    )

    curve_to_mesh_with_uv_result = curve_to_mesh_with_uv(
        curve=curve, profile=capture_attribute.geometry
    )

    input_index_1 = pf.nodes.geo.input_index()

    corners_of_face = pf.nodes.geo.corners_of_face(
        face_index=input_index_1, sort_index=2
    )

    vertex_of_corner = pf.nodes.geo.vertex_of_corner(corners_of_face.corner_index)

    sample_index = pf.nodes.geo.sample_index(
        geometry=curve_to_mesh_with_uv_result.mesh,
        index=vertex_of_corner,
        value=is_edge_point_flat,
    )

    capture_attribute_1 = pf.nodes.geo.capture_attribute(
        geometry=curve_to_mesh_with_uv_result.mesh,
        domain="FACE",
        value=sample_index,
    )

    input_edge_angle = pf.nodes.geo.input_mesh_edge_angle()

    geometry_b = pf.nodes.math.deg_to_rad(20.0)
    geometry_value_a_a = pf.nodes.func.greater_than(
        a=input_edge_angle.unsigned_angle, b=geometry_b
    )
    geometry_value = pf.nodes.func.boolean_or(
        a=geometry_value_a_a, b=capture_attribute_1.value
    )

    store_named_attribute = pf.nodes.geo.store_named_attribute(
        geometry=capture_attribute_1.geometry,
        name="crease_edge",
        value=geometry_value.astype(dtype=float),
        domain="EDGE",
    )
    return RoomEdgeToWallsResult(
        geometry=store_named_attribute,
        vector=curve_to_mesh_with_uv_result.vector,
        is_face_flat=capture_attribute_1.value,
    )


@pf.tracer.grammar
def room_dimensions_distribution(
    rng: pf.RNG,
) -> pf.Vector:
    # pf.random.clip_gaussian(rng, 4.0, 1.5, 5, 8)
    width = pf.random.uniform(rng, 3.5, 8)
    # pf.random.clip_gaussian(rng, 4.5, 1.5, 5, 8)
    depth = pf.random.uniform(rng, 4.5, 8)
    height = pf.random.clip_gaussian(rng, 2.5, 1, 2.2, 4.5)
    dimensions = pf.Vector((width, depth, height))
    return dimensions


def split_flat_walls(
    walls_mesh: pf.MeshObject,
) -> list[pf.MeshObject]:
    wall_idx = pf.ops.attr.read_attribute(walls_mesh, "wall_idx", domain="FACE")
    uniq_idx = np.unique(wall_idx)

    results = []
    for idx in uniq_idx:
        wall_idx = pf.ops.attr.read_attribute(walls_mesh, "wall_idx", domain="FACE")
        is_wall_flat = pf.ops.attr.read_attribute(
            walls_mesh, "is_wall_flat", domain="FACE"
        )
        mask = (wall_idx == idx) * is_wall_flat
        if not mask.any():
            continue
        wall = pf.ops.mesh.separate_mask(walls_mesh, face_mask=mask)
        results.append(wall)

    return results


@pf.tracer.grammar
def room_shape_distribution(
    rng: pf.RNG,
    dimensions: pf.Vector | None = None,
    floor_ceiling_offset_epsilon: float = 0.005,
) -> RoomShapeResult:
    if dimensions is None:
        dimensions = room_dimensions_distribution(rng)

    edge_curve = room_edge_curve_distribution(rng, dimensions=dimensions)

    walls = room_edge_to_walls(
        curve=edge_curve.curve,
        height=dimensions.z,
        is_edge_point_flat=edge_curve.is_flat_point,
    )
    geo = walls.geometry
    geo = pf.nodes.geo.store_named_attribute(
        geometry=geo,
        name="wall_idx",
        value=edge_curve.orig_point_idx,
        domain="FACE",
    )
    geo = pf.nodes.geo.store_named_attribute(
        geometry=geo,
        name="is_wall_flat",
        value=walls.is_face_flat,
        domain="FACE",
    )
    geo = pf.nodes.geo.merge_by_distance(geo, distance=0.001)

    geo = crease_sharp(geo, threshold_degrees=20.0)

    # crease top/bottom edges (edge_neighbors == 1 means boundary edge)
    edge_neighbors = pf.nodes.geo.input_mesh_edge_neighbors()
    is_boundary_edge = pf.nodes.func.equal(a=edge_neighbors, b=1)
    geo = pf.nodes.geo.store_named_attribute(
        geometry=geo,
        name="crease_edge",
        value=1.0,
        domain="EDGE",
        selection=is_boundary_edge,
    )

    # needed for when we split the walls
    geo = pf.nodes.geo.store_named_attribute(
        geometry=geo,
        name="crease_edge",
        value=1.0,
        domain="EDGE",
        selection=walls.is_face_flat,
    )

    walls_mesh = pf.nodes.to_mesh_object(geo)

    eps = floor_ceiling_offset_epsilon
    floor = grid_from_corners(
        point_1=(0, 0, -eps),
        point_2=(dimensions.x, dimensions.y, -eps),
        vertices_x=pf.nodes.math.ceil(dimensions.x),
        vertices_y=pf.nodes.math.ceil(dimensions.y),
    )

    ceiling = grid_from_corners(
        point_1=(0, 0, dimensions.z + eps),
        point_2=(dimensions.x, dimensions.y, dimensions.z + eps),
        vertices_x=pf.nodes.math.ceil(dimensions.x),
        vertices_y=pf.nodes.math.ceil(dimensions.y),
    )
    ceiling = pf.nodes.geo.flip_faces(ceiling)

    floor = pf.nodes.to_mesh_object(floor)
    ceiling = pf.nodes.to_mesh_object(ceiling)

    pf.ops.attr.write_attribute(
        obj=floor,
        data=1.0,
        key="crease_edge",
        domain="EDGE",
    )
    pf.ops.attr.write_attribute(
        ceiling,
        data=1.0,
        key="crease_edge",
        domain="EDGE",
    )

    flat_walls = split_flat_walls(walls_mesh)

    return RoomShapeResult(
        walls=walls_mesh,
        flat_walls=flat_walls,
        floor=floor,
        ceiling=ceiling,
        dimensions=dimensions,
        edge_curve=edge_curve,
    )
