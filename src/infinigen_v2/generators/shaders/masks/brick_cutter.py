from typing import NamedTuple

import numpy as np
import procfunc as pf
from procfunc.nodes import types as t


class BrickCutterResult(NamedTuple):
    mask: pf.ProcNode[float]
    vector: pf.ProcNode[pf.Vector]
    distance_from_edge: pf.ProcNode[float]
    brick_id: pf.ProcNode[pf.Vector]
    random: pf.ProcNode[pf.Color]
    height: pf.ProcNode[float]


@pf.nodes.node_function
def brick_cutter(
    vector: t.SocketOrVal[pf.Vector],
    brick_width: t.SocketOrVal[float] = 0.3,
    brick_height: t.SocketOrVal[float] = 0.1,
    brick_scaling: t.SocketOrVal[float] = 1.0,
    shift: t.SocketOrVal[float] = 0.5,
    irregularity: t.SocketOrVal[float] = 0.0,
    gab: t.SocketOrVal[float] = 0.07,
    round_corner: t.SocketOrVal[float] = 0.0,
    warp_strength: t.SocketOrVal[float] = 0.0,
    warp_size: t.SocketOrVal[float] = 1.0,
    brick_extrusion: t.SocketOrVal[float] = 0.5,
    brick_extrusion_variation: t.SocketOrVal[float] = 0.0,
    brick_bevel: t.SocketOrVal[float] = 0.14,
    brick_bevel_concavity: t.SocketOrVal[float] = 0.5,
    grout_extrusion: t.SocketOrVal[float] = 0.002,
    grout_bevel: t.SocketOrVal[float] = 1.0,
    grout_bevel_concavity: t.SocketOrVal[float] = 0.5,
    seed: t.SocketOrVal[float] = 0.0,
    random_coord: t.SocketOrVal[pf.Vector] = (1.0, 1.0, 1.0),
    masked_coord: t.SocketOrVal[float] = 1.0,
) -> BrickCutterResult:
    # Code generated by procfunc v0.13.1

    brick_id_y_value_a = pf.nodes.math.vector_scale(
        vector=vector, scale=1.0 / brick_scaling
    )

    coord_vector_b_b = pf.nodes.math.combine_xyz(x=brick_width, y=brick_height)

    brick_id_y_value = brick_id_y_value_a / coord_vector_b_b
    brick_id_a_a = pf.nodes.math.fraction(brick_id_y_value.y / 2.0)
    brick_id_a_1 = brick_id_a_a > 0.5
    brick_id_x_value = brick_id_y_value.x + (brick_id_a_1 * shift)
    brick_id_x_1 = pf.nodes.math.floor(brick_id_x_value)
    brick_id_a_0 = pf.nodes.math.fraction(brick_id_x_value)
    brick_id_x_a_b = brick_id_a_0 > irregularity
    brick_id_x_0 = (1.0 - brick_id_x_a_b) * 0.5
    brick_id_y = pf.nodes.math.floor(brick_id_y_value.y)
    brick_id = pf.nodes.math.combine_xyz(x=brick_id_x_1 - brick_id_x_0, y=brick_id_y)

    white_noise = pf.nodes.texture.white_noise(
        vector=brick_id, noise_dimensions="4D", w=seed
    )

    coord_a = white_noise.color.astype(dtype=pf.Vector) - (0.5, 0.5, 0.5)
    coord_vector_a = pf.nodes.math.vector_scale(
        vector=coord_a * random_coord, scale=200.0
    )
    coord_x_0 = brick_id_a_0 - (irregularity * brick_id_x_a_b)
    coord_b_0 = 1.0 - irregularity
    coord_x_b_numerator = pf.nodes.math.mix(
        a=irregularity, b=coord_b_0, factor=brick_id_x_a_b
    )
    coord_x_b = coord_x_b_numerator / 2.0
    coord_y_0 = pf.nodes.math.fraction(brick_id_y_value.y)
    coord_vector_b_a = pf.nodes.math.combine_xyz(
        x=coord_x_0 - coord_x_b, y=coord_y_0 - 0.5
    )
    coord_vector = coord_vector_a + (coord_vector_b_a * coord_vector_b_b)

    noise = pf.nodes.texture.noise(
        vector=coord_vector, scale=50.0 / warp_size, detail=6.0
    )

    distance_from_edge_a_3 = pf.nodes.math.vector_scale(
        vector=noise.color.astype(dtype=pf.Vector) - (0.5, 0.5, 0.5),
        scale=warp_strength * 0.4,
    )

    noise_1 = pf.nodes.texture.noise(
        vector=coord_vector, scale=200.0 / warp_size, detail=4.0
    )

    distance_from_edge_a_2 = pf.nodes.math.vector_scale(
        vector=noise_1.color.astype(dtype=pf.Vector) - (0.5, 0.5, 0.5),
        scale=warp_strength * 0.1,
    )
    distance_from_edge_b_vector_a = pf.nodes.math.combine_xyz(x=coord_x_b, y=0.5)
    distance_from_edge_b_vector_b = pf.nodes.math.vector_absolute(coord_vector_b_a)
    distance_from_edge_b_vector_1 = (
        distance_from_edge_b_vector_a - distance_from_edge_b_vector_b
    )
    distance_from_edge_a_1 = pf.nodes.math.maximum(a=irregularity, b=coord_b_0)
    distance_from_edge_b_scale_numerator = pf.nodes.math.minimum(
        a=distance_from_edge_a_1 * brick_width, b=brick_height
    )
    distance_from_edge_b_scale = distance_from_edge_b_scale_numerator / 2.0
    distance_from_edge_b_1 = pf.nodes.math.vector_scale(
        vector=distance_from_edge_b_vector_1 * coord_vector_b_b,
        scale=1.0 / distance_from_edge_b_scale,
    )
    distance_from_edge_a_a_value = (
        distance_from_edge_a_3 + distance_from_edge_a_2
    ) + distance_from_edge_b_1
    distance_from_edge_a_a = pf.nodes.math.map_range(
        value=distance_from_edge_a_a_value,
        from_max=(1.0, 1.0, 1.0),
        from_min=gab.astype(dtype=pf.Vector),
        to_max=(1.0, 1.0, 1.0),
        to_min=(0.0, 0.0, 0.0),
    )
    distance_from_edge_factor_1 = distance_from_edge_a_a.x < round_corner
    distance_from_edge_factor_0 = distance_from_edge_a_a.y < round_corner
    distance_from_edge_a_0 = pf.nodes.math.minimum(
        a=distance_from_edge_a_a.x, b=distance_from_edge_a_a.y
    )
    distance_from_edge_b_vector_0 = pf.nodes.math.combine_xyz(
        x=round_corner, y=round_corner
    )
    distance_from_edge_b_0 = pf.nodes.math.vector_length(
        distance_from_edge_a_a - distance_from_edge_b_vector_0
    )
    distance_from_edge = pf.nodes.math.mix(
        a=distance_from_edge_a_0,
        b=round_corner - distance_from_edge_b_0,
        factor=distance_from_edge_factor_1 * distance_from_edge_factor_0,
    )

    mask_a_from_max = pf.nodes.math.maximum(a=brick_bevel, b=0.0001)
    mask_a = pf.nodes.math.map_range(value=distance_from_edge, from_max=mask_a_from_max)
    mask = mask_a > 0.0

    coord_scale = pf.nodes.math.mix(a=1.0, b=mask, factor=masked_coord)
    coord_1 = pf.nodes.math.vector_scale(vector=coord_vector, scale=coord_scale)

    height_a_a_1 = brick_extrusion_variation * 0.01
    height_a_3 = (white_noise.fac * height_a_a_1) + (brick_extrusion * 0.01)
    height_a_a_factor = pf.nodes.math.clamp(brick_bevel_concavity + 1.0)
    height_value_2 = pf.nodes.math.acos(1.0 - mask_a)
    height_a_a_a_0 = pf.nodes.math.sin(height_value_2)
    height_a_a_0 = pf.nodes.math.mix(
        a=height_a_a_a_0, b=mask_a, factor=height_a_a_factor
    )
    height_a_b_value = pf.nodes.math.acos(mask_a)
    height_a_b_0 = pf.nodes.math.sin(height_a_b_value)
    height_a_2 = pf.nodes.math.mix(
        a=height_a_a_0,
        b=1.0 - height_a_b_0,
        factor=brick_bevel_concavity,
    )
    height_b_4 = grout_extrusion * 0.01
    height_factor = pf.nodes.math.clamp(grout_bevel_concavity + 1.0)
    height_a_1 = pf.nodes.math.maximum(
        a=distance_from_edge_a_a_value.x,
        b=distance_from_edge_a_a_value.y,
    )
    height_b_b_1 = gab * round_corner
    height_b_3 = (gab + round_corner) - height_b_b_1
    height_value_1 = pf.nodes.math.minimum(
        a=distance_from_edge_a_a_value.x,
        b=distance_from_edge_a_a_value.y,
    )
    height_b_value_a = pf.nodes.math.map_range(value=height_value_1, from_max=gab)
    height_value_vector = pf.nodes.math.combine_xyz(x=height_b_3, y=height_b_3)
    height_value_0 = pf.nodes.math.vector_length(
        distance_from_edge_a_a_value - height_value_vector
    )
    height_from_max_value_1 = (height_b_3 - height_a_1) ** 2.0
    height_from_max_value_0 = height_b_3**2.0
    height_from_max = pf.nodes.math.sqrt(
        height_from_max_value_1 + height_from_max_value_0
    )
    height_b_value_b = pf.nodes.math.map_range(
        value=height_value_0,
        from_max=height_from_max,
        from_min=round_corner - height_b_b_1,
        to_max=0.0,
        to_min=1.0,
    )
    height_b_value_1 = pf.nodes.math.mix(
        a=height_b_value_a,
        b=height_b_value_b,
        factor=height_a_1 < height_b_3,
    )
    height_b_from_min = pf.nodes.math.maximum(a=grout_bevel, b=0.0001)
    height_b_2 = pf.nodes.math.map_range(
        value=height_b_value_1, from_min=1.0 - height_b_from_min
    )
    height_a_value = pf.nodes.math.acos(1.0 - height_b_2)
    height_a_0 = pf.nodes.math.sin(height_a_value)
    height_b_b_a = pf.nodes.math.mix(a=height_a_0, b=height_b_2, factor=height_factor)
    height_b_value_0 = pf.nodes.math.acos(height_b_2)
    height_b_1 = pf.nodes.math.sin(height_b_value_0)
    height_b_b_0 = pf.nodes.math.mix(
        a=height_b_b_a,
        b=1.0 - height_b_1,
        factor=grout_bevel_concavity,
    )
    height_b_0 = 1.0 - height_b_b_0
    height = (height_a_3 * height_a_2) + (height_b_4 * height_b_0)
    return BrickCutterResult(
        mask=mask,
        vector=coord_1,
        distance_from_edge=distance_from_edge,
        brick_id=brick_id,
        random=white_noise.color,
        height=height,
    )


def brick_shift_distribution(
    rng: pf.RNG,
) -> t.SocketOrVal[float]:
    shift = pf.control.choice(
        rng,
        [
            (0.5, 2.0),  # vast majority of realworld bricks are half-overlapping
            (pf.random.uniform(rng, 0.0, 1.0), 0.5),
        ],
    )
    return shift


def brick_dimensions_distribution(
    rng: pf.RNG,
    brick_width_mean: t.SocketOrVal[float] = 0.3,
    brick_width_std: t.SocketOrVal[float] = 0.1,
    brick_aspect_mean: t.SocketOrVal[float] = 0.4,
    brick_aspect_std: t.SocketOrVal[float] = 0.1,
) -> pf.ProcNode[pf.Vector]:
    """
    Dimensions of typical red/gray bricks from an industrial factory
    (as opposed to a large quarried stone block)
    """

    brick_width = pf.random.clip_gaussian(
        rng, brick_width_mean, brick_width_std, 0.1, 0.5
    )
    brick_aspect = pf.random.clip_gaussian(
        rng, brick_aspect_mean, brick_aspect_std, 0.2, 1.0
    )
    brick_height = brick_width * brick_aspect
    return pf.nodes.math.combine_xyz(x=brick_width, y=brick_height)


def brick_cutter_distribution(
    rng: pf.RNG,
    vector: t.SocketOrVal[pf.Vector],
    irregularity: t.SocketOrVal[float] | None = None,
    warp_strength: t.SocketOrVal[float] | None = None,
    gab: t.SocketOrVal[float] | None = None,
    brick_extrusion: t.SocketOrVal[float] | None = None,
    brick_dimensions: t.SocketOrVal[pf.Vector] | None = None,
    grout_extrusion: t.SocketOrVal[float] | None = None,
) -> BrickCutterResult:
    # Code generated by procfunc v0.3.0, tweaked by araistrick

    r1, r2, rng = rng.spawn(3)

    if brick_dimensions is None:
        brick_dimensions = brick_dimensions_distribution(r1)
    brick_dim_sep = pf.nodes.math.separate_xyz(vector=brick_dimensions)

    # irregularity adds or subtracts from the absolute width of the brick, so needs to be relative
    if irregularity is None:
        # irregularity == 0 breaks brick_selector, use eps=0.001 instead
        rand = pf.random.uniform(rng, 0.3, 0.45)
        irregularity = pf.control.choice(
            r2,
            [(0.001, 1.0), (rand, 1.0)],
        )

    # values actually at 0 look very weird, so pick a side
    if grout_extrusion is None:
        grout_extrusion = pf.random.uniform(rng, -1, 0.4)

    #  concavity and extrusion are coupled - positive extrusion only works with positive concavity
    grout_bevel_concavity = pf.random.uniform(rng, 0, 1) * np.sign(grout_extrusion)
    grout_bevel = pf.random.uniform(rng, 0.4, 1.0)

    # TODO possibly needs to be coupled to be not similar to grout_extrusion
    if brick_extrusion is None:
        brick_extrusion = pf.random.uniform(rng, 0.2, 1.5)
    brick_extrusion_variation = pf.random.uniform(rng, 0.0, 1.0)
    brick_bevel = pf.random.uniform(rng, 0.05, 0.5)

    brick_bevel_concavity = pf.random.uniform(rng, -1, 0.2)

    shift = 0.5  # how much smaller are some bricks than others ?
    brick_scaling = pf.random.uniform(rng, 1, 2)

    # spacing between bricks, in units = percentage of height
    if gab is None:
        gab = pf.random.uniform(rng, 0.05, 0.2)

    round_corner = pf.random.clip_gaussian(rng, 0.3, 0.15, 0.0, 0.5)

    if warp_strength is None:
        warp_strength = pf.random.uniform(rng, 0.2, 1.2)
    warp_size = pf.random.uniform(rng, 1, 2.5)

    brick_cutter_result = brick_cutter(
        vector=vector,
        brick_width=brick_dim_sep.x,
        brick_height=brick_dim_sep.y,
        brick_scaling=brick_scaling,
        shift=shift,
        irregularity=irregularity,
        gab=gab,
        round_corner=round_corner,
        warp_strength=warp_strength,
        warp_size=warp_size,
        brick_extrusion=brick_extrusion,
        brick_extrusion_variation=brick_extrusion_variation,
        brick_bevel=brick_bevel,
        brick_bevel_concavity=brick_bevel_concavity,
        grout_extrusion=grout_extrusion,
        grout_bevel=grout_bevel,
        grout_bevel_concavity=grout_bevel_concavity,
        seed=pf.random.uniform(rng, -1000, 1000),
    )
    return brick_cutter_result
