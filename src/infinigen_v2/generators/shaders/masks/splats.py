def splat_dots_distribution(
    rng: pf.RNG,
    vector: t.SocketOrVal[pf.Vector],
    spread: t.SocketOrVal[float] | None = None,
    opacity: t.SocketOrVal[float] | None = None,
    size: t.SocketOrVal[float] | None = None,
):
    if spread is None:
        spread = pf.random.clip_gaussian(rng, 0.15, 0.07, 0.05, 0.4)
    if opacity is None:
        opacity = pf.random.clip_gaussian(rng, 0.15, 0.2, 0.05, 0.7)
    if size is None:
        size = pf.random.uniform(rng, 0.05, 0.3)

    detail = pf.control.choice(rng, [(0.0, 2.0), (1.0, 1.0), (2.0, 0.5)])
    smoothness = pf.random.uniform(rng, 0.262597, 0.41376)
    distortion_size = pf.random.uniform(rng, 2.0, 3.0)
    distortion_strength = pf.random.uniform(rng, 1.0, 3.0)
    distortion_detail = 4.0
    opacity_variation = pf.random.uniform(rng, 0.333333, 1.0)
    seed = pf.random.uniform(rng, 0.0, 100.0)
    noise_settings_use_noise = pf.control.choice(rng, [(0.0, 1.0), (1.0, 1.0)])
    noise_settings_noise_size = 0.3
    noise_settings_noise_detail = 4.0
    border_settings_border_thickness = 0.636047
    border_settings_border_opacity = 0.0

    return splat_dots(
        vector=vector,
        size=size,
        detail=detail,
        spread=spread,
        smoothness=smoothness,
        distortion_size=distortion_size,
        distortion_strength=distortion_strength,
        distortion_detail=distortion_detail,
        opacity=opacity,
        opacity_variation=opacity_variation,
        seed=seed,
        noise_settings_use_noise=noise_settings_use_noise,
        noise_settings_noise_size=noise_settings_noise_size,
        noise_settings_noise_detail=noise_settings_noise_detail,
        border_settings_border_thickness=border_settings_border_thickness,
        border_settings_border_opacity=border_settings_border_opacity,
    )


