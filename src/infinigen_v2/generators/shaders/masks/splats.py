def uv_gradient_distribution(
    rng: pf.RNG,
    vector: t.SocketOrVal[pf.Vector],
    gradient_fac: t.SocketOrVal[float] | None = None,
    gradient_start: t.SocketOrVal[float] | None = None,
    gradient_end: t.SocketOrVal[float] | None = None,
) -> pf.ProcNode[float]:
    if gradient_fac is None:
        gradient_fac = vector.y
    if gradient_start is None:
        gradient_start = pf.control.choice(rng, [(0.0, 0.5), (1.0, 0.5)])
    if gradient_end is None:
        gradient_end = pf.random.uniform(rng, 0.0, 1.0)

    return pf.nodes.math.map_range(
        value=gradient_fac,
        from_min=gradient_start,
        from_max=gradient_end,
    )


def splats_mask_distribution(
    rng: pf.RNG,
    vector: t.SocketOrVal[pf.Vector],
    size: t.SocketOrVal[float] | None = None,
    allow_gradient: bool = False,
    gradient_fac: t.SocketOrVal[float] | None = None,
    gradient_start: t.SocketOrVal[float] | None = None,
    gradient_end: t.SocketOrVal[float] | None = None,
) -> SplatsMaskResult:
    if size is None:
        size = pf.random.clip_gaussian(rng, 2, 4, 0.5, 10)

    initial_size = size * pf.random.uniform(rng, 0.5, 1.5)
    mask = splat_dots_distribution(rng=rng, vector=vector, size=initial_size)
    mask = mask + splat_dots_distribution(rng=rng, vector=vector)

    for _i in range(3):
        size = size * pf.random.uniform(rng, 0.5, 1.5)
        newmask = pf.control.choice(
            rng,
            [
                (splat_dots_distribution, 1.0),
                (splats_streaks_distribution, 1.0),
                (lambda *_, **__: 0.0, 1.0),
            ],
        )(rng, vector, size=size)
        mask = mask + newmask

    if allow_gradient:
        gradient = uv_gradient_distribution(
            rng=rng,
            vector=vector,
            gradient_fac=gradient_fac,
            gradient_start=gradient_start,
            gradient_end=gradient_end,
        )
        mask = pf.nodes.math.clamp(mask * gradient)

    return SplatsMaskResult(mask=mask)
