def _sample_pair_bounded(
    rng: pf.RNG, min_val: float, max_val: float, std: float = 0.08
) -> tuple[float, float]:
    a = pf.random.clip_gaussian(rng, min_val, std, 0.0, 1.0)
    b = pf.random.clip_gaussian(rng, max_val, std, 0.0, 1.0)
    return min(a, b), max(a, b)


