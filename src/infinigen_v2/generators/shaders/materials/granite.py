def _create_gradient_color(
    rng: pf.RNG,
    vector: t.SocketOrVal[pf.Vector],
    base_color: pf.Color,
) -> pf.ProcNode[pf.Color]:
    """Create a gradient color pattern using space_warp and voronoi.

    This creates varied stone-like color patterns similar to the demo functions,
    blending 4 shades derived from the base color.
    """
    # Apply two space_warp operations to create organic distortion
    warp_strength_1 = pf.random.uniform(rng, 10.0, 20.0)
    warp_strength_2 = pf.random.uniform(rng, 15.0, 25.0)
    warp_w = pf.random.uniform(rng, 0.0, 50.0)

    space_warp_result = space_warp(
        vector=vector,
        strength=warp_strength_1,
        w=0.0,
        size=pf.random.uniform(rng, 0.03, 0.08),
        detail=pf.random.uniform(rng, 3.0, 7.0),
        roughness=pf.random.uniform(rng, 0.6, 0.8),
        lacunarity=2.0,
        distortion=0.0,
    )
    space_warp_result_1 = space_warp(
        vector=space_warp_result.vector,
        strength=warp_strength_2,
        w=warp_w,
        size=pf.random.uniform(rng, 0.01, 0.04),
        detail=pf.random.uniform(rng, 1.5, 3.5),
        roughness=pf.random.uniform(rng, 0.7, 0.85),
        lacunarity=2.0,
        distortion=0.0,
    )

    # Create voronoi pattern
    voronoi_scale = pf.random.uniform(rng, 5.0, 15.0)
    voronoi = pf.nodes.shader.voronoi(
        vector=space_warp_result_1.vector,
        scale=voronoi_scale,
        detail=pf.random.uniform(rng, 2.0, 4.0),
        normalize=True,
    )

    # Map voronoi distance to color gradient
    from_min = pf.random.uniform(rng, 0.05, 0.25)
    from_max = pf.random.uniform(rng, 0.35, 0.55)
    color_mix_4_map = pf.nodes.func.map_range(
        value=voronoi.distance, from_max=from_max, from_min=from_min
    )

    # Derive 4 color shades from the base color using HSV manipulation
    # Color 1: Darker shade
    c1_v = max(0.01, base_color.v * pf.random.uniform(rng, 0.1, 0.3))
    color_1 = pf.color.hsv_color((base_color.h, base_color.s * 0.8, c1_v))

    # Color 2: Medium-dark shade
    c2_v = max(0.02, base_color.v * pf.random.uniform(rng, 0.4, 0.7))
    color_2 = pf.color.hsv_color((base_color.h, base_color.s, c2_v))

    # Color 3: Base shade (close to original)
    c3_v = base_color.v * pf.random.uniform(rng, 0.8, 1.1)
    color_3 = pf.color.hsv_color((base_color.h, base_color.s, min(1.0, c3_v)))

    # Color 4: Lighter/grayer shade
    c4_s = base_color.s * pf.random.uniform(rng, 0.3, 0.7)
    c4_v = pf.random.uniform(rng, 0.1, 0.5)
    color_4 = pf.color.hsv_color((base_color.h, c4_s, c4_v))

    # Randomize gradient positions
    pos_2 = pf.random.uniform(rng, 0.1, 0.25)
    pos_3 = pf.random.uniform(rng, 0.35, 0.55)
    pos_4 = pf.random.uniform(rng, 0.7, 0.95)

    return color_mix_4(
        map=color_mix_4_map,
        color_1=color_1,
        color_1_position=0.0,
        color_2=color_2,
        color_2_position=pos_2,
        color_3=color_3,
        color_3_position=pos_3,
        color_4=color_4,
        color_4_position=pos_4,
    )


