class FieldsparResult(NamedTuple):
    alpha: pf.ProcNode[float]
    color: pf.ProcNode[pf.Color]
    height: pf.ProcNode[float]
    chisel: pf.ProcNode[pf.Color]


@pf.nodes.node_function
def fieldspar(
    vector: t.SocketOrVal[pf.Vector] = (0.0, 0.0, 0.0),
    w: t.SocketOrVal[float] = 0.0,
    size: t.SocketOrVal[float] = 1.0,
    spread: t.SocketOrVal[float] = 0.0,
    smoothness: t.SocketOrVal[float] = 0.0,
    color: t.SocketOrVal[pf.Color] = pf.Color((0.5, 0.183, 0.121)),
    color_variation: t.SocketOrVal[float] = 1.0,
    height_scale: t.SocketOrVal[float] = 1.0,
) -> FieldsparResult:
    # Code generated by procfunc v0.2.0

    space_warp_result = space_warp(
        vector=vector / size.astype(dtype=pf.Vector), w=w, size=0.003, detail=3.0
    )
    space_warp_result_1 = space_warp(
        vector=space_warp_result.vector, strength=2.19, w=w, size=0.005, detail=1.0
    )

    alpha_value_1 = pf.nodes.shader.voronoi(
        vector=space_warp_result_1.vector,
        w=w,
        scale=1.0 / 0.0067,
        detail=4.0,
        voronoi_dimensions="4D",
    )
    alpha_value = pf.nodes.func.float_curve(
        value=spread,
        curve=np.array([[0.0, 0.0], [0.4, 0.7313], [1.0, 1.0]]),
    )
    alpha_1 = pf.nodes.func.map_range(value=alpha_value, to_min=0.25, to_max=1.64)
    alpha_from_min = pf.nodes.func.mix(factor=smoothness, a=alpha_1 - 0.0001, b=0.0)
    alpha = pf.nodes.func.map_range(
        value=alpha_value_1.distance,
        from_min=alpha_from_min,
        from_max=alpha_1,
        to_min=1.0,
        to_max=0.0,
    )

    color_vector = pf.nodes.shader.voronoi(
        vector=space_warp_result_1.vector,
        w=w,
        scale=1.0 / 0.0067,
        voronoi_dimensions="4D",
    )

    space_warp_result_2 = space_warp(
        vector=vector / size.astype(dtype=pf.Vector),
        strength=3.0,
        w=w,
        size=0.001,
        detail=5.0,
        roughness=0.7417,
        lacunarity=1.5,
    )
    space_warp_result_3 = space_warp(
        vector=space_warp_result_2.vector,
        strength=8.0,
        w=w,
        size=0.05,
        detail=13.7,
        roughness=0.8833,
        lacunarity=1.5,
    )

    color_value_value = pf.nodes.shader.voronoi(
        vector=color_vector.color.astype(dtype=pf.Vector) + space_warp_result_3.vector,
        w=w,
        scale=30.34,
        detail=1.0,
        roughness=0.7529,
        lacunarity=1.6,
        voronoi_dimensions="4D",
    )
    color_value_2 = pf.nodes.func.map_range(
        value=color_value_value.distance, from_min=0.59, from_max=1.9
    )
    color_hue_value = pf.nodes.func.float_curve(
        value=color_value_2,
        curve=np.array([[0.0, 0.0], [0.3045, 0.1813], [0.7364, 0.7063], [1.0, 1.0]]),
    )
    color_hue = pf.nodes.func.map_range(value=color_hue_value, to_min=0.48, to_max=0.52)
    color_saturation = pf.nodes.func.map_range(
        value=color_hue_value, to_min=0.9, to_max=1.1
    )
    color_value_1 = pf.nodes.func.map_range(
        value=color_hue_value, to_min=0.8, to_max=2.0
    )

    height_3 = pf.nodes.shader.noise(
        vector=alpha_value_1.position,
        w=alpha_value_1.w,
        scale=267.4,
        detail=5.0,
        noise_dimensions="4D",
    )

    color_color_factor = pf.nodes.func.map_range(
        value=height_3.fac, from_min=0.4, from_max=0.6
    )
    color_color_5 = pf.nodes.func.rgb_curve(
        color=color,
        curves=[
            np.array([[0.0, 0.0], [0.5091, 0.5625], [1.0, 1.0]]),
            np.array([[0.0, 0.0], [1.0, 1.0]]),
            np.array([[0.0, 0.0], [1.0, 1.0]]),
            np.array([[0.0, 0.0], [0.5455, 0.3688], [1.0, 1.0]]),
        ],
    )
    color_color_4 = pf.nodes.func.mix_rgb(
        factor=color_color_factor, a=color, b=color_color_5
    )
    color_value = pf.nodes.shader.voronoi_distance(
        vector=vector / size.astype(dtype=pf.Vector),
        w=w,
        scale=1.0 / 0.0022,
        detail=2.0,
        roughness=0.7529,
        lacunarity=1.6,
        voronoi_dimensions="4D",
    )
    color_color_value = pf.nodes.func.map_range(
        value=color_value, from_max=0.11, to_min=1.0, to_max=0.0
    )
    color_color_3 = pf.nodes.func.float_curve(
        value=color_color_value,
        curve=np.array([[0.0, 0.0], [0.6864, 0.2187], [1.0, 1.0]]),
    )
    color_color_2 = pf.nodes.func.mix_rgb(
        factor=0.4083,
        a=color_color_4,
        b=color_color_3.astype(dtype=pf.Color),
        blend_type="SOFT_LIGHT",
    )

    fractal_chip_result = fractal_chip(
        vector=space_warp_result_2.vector,
        w=w,
        size=0.006,
        levels=2.0,
        fractal_step_size=1.5,
    )

    color_color_1 = pf.nodes.func.map_range(
        value=fractal_chip_result.height, from_min=0.1, from_max=0.25
    )
    color_color = pf.nodes.func.mix_rgb(
        factor=0.5333,
        a=color_color_2,
        b=color_color_1.astype(dtype=pf.Color),
        blend_type="SOFT_LIGHT",
    )
    color_1 = pf.nodes.shader.hue_saturation(
        hue=color_hue,
        saturation=color_saturation,
        value=color_value_1,
        fac=color_variation,
        color=color_color,
    )

    height_2 = color_value_2 - 0.5
    height_1 = height_3.fac + (height_2 * 0.5)
    height = (height_scale * height_1) + 3.0

    chisel = pf.nodes.shader.voronoi(
        vector=vector / size.astype(dtype=pf.Vector),
        w=w + 15.1,
        scale=1000.0,
        roughness=0.7529,
        lacunarity=1.6,
        voronoi_dimensions="4D",
    )
    return FieldsparResult(
        alpha=alpha,
        color=color_1,
        height=height,
        chisel=chisel.color,
    )


class QuartzResult(NamedTuple):
    alpha: pf.ProcNode[float]
    color: pf.ProcNode[pf.Color]
    depth: pf.ProcNode[float]
    height: pf.ProcNode[float]
    chisel: pf.ProcNode[pf.Color]


@pf.nodes.node_function
def quartz(
    vector: t.SocketOrVal[pf.Vector] = (0.0, 0.0, 0.0),
    w: t.SocketOrVal[float] = 0.0,
    size: t.SocketOrVal[float] = 1.0,
    spread: t.SocketOrVal[float] = 0.0,
    smoothness: t.SocketOrVal[float] = 0.2,
    color: t.SocketOrVal[pf.Color] = pf.Color((0.255, 0.255, 0.255)),
    color_variation: t.SocketOrVal[float] = 1.0,
    height_scale: t.SocketOrVal[float] = 1.0,
) -> QuartzResult:
    # Code generated by procfunc v0.2.0

    space_warp_result = space_warp(
        vector=vector / size.astype(dtype=pf.Vector),
        strength=2.0,
        w=w,
        size=0.001,
        detail=3.0,
        roughness=0.7417,
        lacunarity=1.5,
    )

    fractal_chip_result = fractal_chip(
        vector=space_warp_result.vector,
        w=w + 50.0,
        size=0.0043,
        levels=3.0,
        fractal_step_size=1.5,
    )

    alpha_value = pf.nodes.func.float_curve(
        value=spread,
        curve=np.array([[0.0, 0.0], [0.4, 0.7313], [1.0, 1.0]]),
    )
    alpha_1 = pf.nodes.func.map_range(value=alpha_value, to_min=0.61, to_max=-0.04)
    alpha_from_max = pf.nodes.func.mix(factor=smoothness, a=alpha_1 + 0.0001, b=1.0)
    alpha = pf.nodes.func.map_range(
        value=fractal_chip_result.height,
        from_min=alpha_1,
        from_max=alpha_from_max,
    )

    chisel_vector_1 = pf.nodes.shader.voronoi(
        vector=vector / size.astype(dtype=pf.Vector),
        w=w,
        scale=700.0,
        roughness=1.0,
        lacunarity=0.98,
        voronoi_dimensions="4D",
    )
    chisel_vector = chisel_vector_1.color.astype(dtype=pf.Vector) - (0.5, 0.5, 0.5)

    space_warp_result_1 = space_warp(
        vector=vector / size.astype(dtype=pf.Vector),
        strength=8.0,
        w=w,
        size=0.02,
        detail=15.0,
        roughness=0.8417,
    )

    color_hue_value = pf.nodes.shader.voronoi(
        vector=chisel_vector + space_warp_result_1.vector,
        w=w,
        scale=100.0,
        detail=1.0,
        roughness=0.7,
        lacunarity=1.5,
        voronoi_dimensions="4D",
    )
    color_hue_1 = pf.nodes.func.map_range(
        value=color_hue_value.distance, from_min=0.81, from_max=1.89
    )
    color_hue = pf.nodes.func.map_range(value=color_hue_1, to_min=0.3, to_max=0.7)
    color_saturation = pf.nodes.func.map_range(
        value=color_hue_1, to_min=0.85, to_max=1.3
    )
    color_value_1 = pf.nodes.func.map_range(value=color_hue_1, to_min=0.1, to_max=2.0)
    color_5 = pf.nodes.shader.hue_saturation(
        hue=color_hue,
        saturation=color_saturation,
        value=color_value_1,
        fac=color_variation,
        color=color,
    )
    color_4 = chisel_vector_1.color.astype(dtype=pf.Vector)
    color_3 = pf.nodes.func.mix_rgb(
        factor=0.6,
        a=color_5,
        b=color_4.x.astype(dtype=pf.Color),
        blend_type="SOFT_LIGHT",
    )
    color_value_value = pf.nodes.shader.voronoi_distance(
        vector=chisel_vector + vector / size.astype(dtype=pf.Vector),
        w=w,
        scale=529.7,
        detail=0.85,
        roughness=1.0,
        lacunarity=0.98,
        voronoi_dimensions="4D",
    )
    color_value = pf.nodes.func.map_range(
        value=color_value_value, from_max=0.07, to_min=1.0, to_max=0.0
    )
    color_2 = pf.nodes.func.float_curve(
        value=color_value,
        curve=np.array([[0.0, 0.0], [0.7955, 0.2313], [1.0, 1.0]]),
    )
    color_1 = pf.nodes.func.mix_rgb(
        factor=0.6,
        a=color_3,
        b=color_2.astype(dtype=pf.Color),
        blend_type="SOFT_LIGHT",
    )

    depth = pf.nodes.shader.voronoi_distance(
        vector=vector / size.astype(dtype=pf.Vector),
        w=w,
        scale=300.0,
        roughness=0.6583,
        lacunarity=0.98,
        voronoi_dimensions="4D",
    )

    height_2 = color_hue_1 - 0.5
    height_1 = fractal_chip_result.height + (height_2 * 0.1)
    height = (height_scale * height_1) + 2.0

    chisel = pf.nodes.shader.voronoi(
        vector=chisel_vector + vector / size.astype(dtype=pf.Vector),
        w=w,
        scale=900.0,
        roughness=0.6583,
        lacunarity=0.98,
        voronoi_dimensions="4D",
    )
    return QuartzResult(
        alpha=alpha,
        color=color_1,
        depth=depth,
        height=height,
        chisel=chisel.color,
    )


class MicaHornblendeResult(NamedTuple):
    alpha: pf.ProcNode[float]
    color: pf.ProcNode[pf.Color]
    height: pf.ProcNode[float]
    chisel: pf.ProcNode[pf.Color]


@pf.nodes.node_function
def mica_hornblende(
    vector: t.SocketOrVal[pf.Vector] = (0.0, 0.0, 0.0),
    w: t.SocketOrVal[float] = 0.0,
    size: t.SocketOrVal[float] = 1.0,
    spread: t.SocketOrVal[float] = 0.0,
    smoothness: t.SocketOrVal[float] = 0.2,
    color: t.SocketOrVal[pf.Color] = pf.Color((0.5, 0.5, 0.5)),
    color_variation: t.SocketOrVal[float] = 1.0,
    height_scale: t.SocketOrVal[float] = 1.0,
) -> MicaHornblendeResult:
    # Code generated by procfunc v0.2.0

    space_warp_result = space_warp(
        vector=vector / size.astype(dtype=pf.Vector),
        w=w,
        size=0.001,
        detail=3.0,
        roughness=0.6583,
    )

    fractal_chip_result = fractal_chip(
        vector=space_warp_result.vector,
        w=w + 50.0,
        size=0.0029,
        levels=5.0,
        fractal_step_size=1.5,
    )

    alpha_value = pf.nodes.func.float_curve(
        value=spread,
        curve=np.array([[0.0, 0.0], [0.4, 0.7313], [1.0, 1.0]]),
    )
    alpha_1 = pf.nodes.func.map_range(value=alpha_value, to_min=0.4, to_max=0.0)
    alpha_from_max = pf.nodes.func.mix(factor=smoothness, a=alpha_1 + 0.0001, b=1.0)
    alpha = pf.nodes.func.map_range(
        value=fractal_chip_result.height,
        from_min=alpha_1,
        from_max=alpha_from_max,
    )

    color_vector_scale = pf.nodes.func.constant(464.3999938964844)
    color_vector_1 = pf.nodes.shader.voronoi(
        vector=space_warp_result.vector,
        w=w,
        scale=color_vector_scale,
        roughness=0.7833,
        voronoi_dimensions="4D",
    )
    color_vector = color_vector_1.color.astype(dtype=pf.Vector) - (0.5, 0.5, 0.5)

    space_warp_result_1 = space_warp(
        vector=space_warp_result.vector,
        strength=8.0,
        w=w,
        size=0.05,
        detail=15.0,
        roughness=0.9167,
    )

    color_hue_value = pf.nodes.shader.voronoi(
        vector=color_vector + space_warp_result_1.vector,
        w=w,
        scale=33.8,
        detail=1.0,
        roughness=0.7,
        lacunarity=1.5,
        voronoi_dimensions="4D",
    )
    color_hue_1 = pf.nodes.func.map_range(
        value=color_hue_value.distance, from_min=0.59, from_max=2.0
    )
    color_hue = pf.nodes.func.map_range(value=color_hue_1, to_min=0.3, to_max=0.7)
    color_saturation = pf.nodes.func.map_range(
        value=color_hue_1, to_min=0.9, to_max=1.3
    )
    color_value = pf.nodes.func.map_range(value=color_hue_1, to_min=0.8, to_max=2.0)
    color_3 = pf.nodes.shader.hue_saturation(
        hue=color_hue,
        saturation=color_saturation,
        value=color_value,
        fac=color_variation,
        color=color,
    )
    color_2 = color_vector_1.color.astype(dtype=pf.Vector)
    color_1 = pf.nodes.func.mix_rgb(
        factor=0.25,
        a=color_3,
        b=color_2.x.astype(dtype=pf.Color),
        blend_type="SOFT_LIGHT",
    )

    height_2 = color_hue_1 - 0.5
    height_1 = fractal_chip_result.height + (height_2 * 0.1)
    height = (height_scale * height_1) + 1.0

    chisel = pf.nodes.shader.voronoi(
        vector=space_warp_result.vector,
        w=w,
        scale=1000.0,
        roughness=0.7,
        lacunarity=1.5,
        voronoi_dimensions="4D",
    )
    return MicaHornblendeResult(
        alpha=alpha,
        color=color_1,
        height=height,
        chisel=chisel.color,
    )


class GraniteResult(NamedTuple):
    surface: pf.ProcNode[pf.Shader]
    displacement: pf.ProcNode[pf.Vector]
    color: pf.ProcNode[pf.Color]


@pf.nodes.node_function
def granite(
    vector: t.SocketOrVal[pf.Vector] = (-999999.0, -999999.0, -999999.0),
    w: t.SocketOrVal[float] = 0.0,
    size: t.SocketOrVal[float] = 0.05,
    min_roughness: t.SocketOrVal[float] = 0.3,
    max_roughness: t.SocketOrVal[float] = 0.5,
    min_spec: t.SocketOrVal[float] = 0.2,
    max_spec: t.SocketOrVal[float] = 0.4,
    height_scale: t.SocketOrVal[float] = 1.0,
    height_offset: t.SocketOrVal[float] = 0.0,
    hornblende_size: t.SocketOrVal[float] = 1.0,
    hornblende_color: t.SocketOrVal[pf.Color] = pf.Color((0.009, 0.01, 0.011)),
    hornblende_color_variation: t.SocketOrVal[float] = 1.0,
    hornblende_height: t.SocketOrVal[float] = 1.0,
    mica_size: t.SocketOrVal[float] = 1.0,
    mica_spread: t.SocketOrVal[float] = 0.8833,
    mica_smoothness: t.SocketOrVal[float] = 0.2,
    mica_color: t.SocketOrVal[pf.Color] = pf.Color((0.011, 0.013, 0.014)),
    mica_color_variation: t.SocketOrVal[float] = 1.0,
    mica_height: t.SocketOrVal[float] = 1.0,
    quartz_size: t.SocketOrVal[float] = 1.0,
    quartz_spread: t.SocketOrVal[float] = 0.7917,
    quartz_smoothness: t.SocketOrVal[float] = 0.2,
    quartz_color: t.SocketOrVal[pf.Color] = pf.Color((0.028, 0.038, 0.049)),
    quartz_color_variation: t.SocketOrVal[float] = 1.0,
    quartz_height: t.SocketOrVal[float] = 1.0,
    feldspar_size: t.SocketOrVal[float] = 1.0,
    feldspar_spread: t.SocketOrVal[float] = 0.5417,
    feldspar_smoothness: t.SocketOrVal[float] = 0.02,
    feldspar_color: t.SocketOrVal[pf.Color] = pf.Color((0.006, 0.007, 0.008)),
    feldspar_color_variation: t.SocketOrVal[float] = 1.0,
    feldspar_height: t.SocketOrVal[float] = 1.0,
) -> GraniteResult:
    # Code generated by procfunc v0.2.0

    space_warp_result = space_warp(
        vector=vector / size.astype(dtype=pf.Vector),
        strength=3.0,
        size=0.0001,
        detail=3.0,
    )

    mica_hornblende_result = mica_hornblende(
        vector=space_warp_result.vector,
        w=w / size,
        size=mica_size,
        spread=mica_spread,
        smoothness=mica_smoothness,
        color=mica_color,
        color_variation=mica_color_variation,
        height_scale=mica_height,
    )

    quartz_result = quartz(
        vector=space_warp_result.vector,
        w=w / size,
        size=quartz_size,
        spread=quartz_spread,
        smoothness=quartz_smoothness,
        color=quartz_color,
        color_variation=quartz_color_variation,
        height_scale=quartz_height,
    )

    fieldspar_result = fieldspar(
        vector=space_warp_result.vector,
        w=w / size,
        size=feldspar_size,
        spread=feldspar_spread,
        smoothness=feldspar_smoothness,
        color=feldspar_color,
        color_variation=feldspar_color_variation,
        height_scale=feldspar_height,
    )

    values_layering_5_result = values_layering_5(
        layer_2_alpha=1.0,
        layer_2=0.4,
        layer_3_alpha=mica_hornblende_result.alpha,
        layer_3=1.0,
        layer_4_alpha=quartz_result.alpha,
        layer_4=1.0,
        layer_5_alpha=fieldspar_result.alpha,
        layer_5=0.7,
    )

    space_warp_result_1 = space_warp(
        vector=space_warp_result.vector,
        strength=8.0,
        w=w / size,
        size=0.02,
        detail=16.0,
        roughness=0.9,
    )

    surface_value_value = pf.nodes.shader.voronoi(
        vector=space_warp_result_1.vector,
        w=w / size,
        scale=59.61,
        voronoi_dimensions="4D",
    )
    surface_value_1 = pf.nodes.func.map_range(
        value=surface_value_value.distance, from_min=0.32
    )
    surface_value = surface_value_1 * 0.5
    surface_fac_value = pf.nodes.math.clamp(values_layering_5_result - surface_value)
    surface_fac = pf.nodes.func.map_range(
        value=surface_fac_value, to_min=min_spec, to_max=max_spec
    )

    mica_hornblende_result_1 = mica_hornblende(
        vector=space_warp_result.vector,
        w=w / size,
        size=hornblende_size,
        spread=1.0,
        smoothness=0.0,
        color=hornblende_color,
        color_variation=hornblende_color_variation,
        height_scale=hornblende_height,
    )

    colors_layering_5_result = colors_layering_5(
        layer_2_alpha=1.0,
        color_2=mica_hornblende_result_1.color,
        layer_3_alpha=mica_hornblende_result.alpha,
        color_3=mica_hornblende_result.color,
        layer_4_alpha=quartz_result.alpha,
        color_4=quartz_result.color,
        layer_5_alpha=fieldspar_result.alpha,
        color_5=fieldspar_result.color,
    )

    surface_shader_1 = pf.nodes.shader.diffuse_bsdf(color=colors_layering_5_result)

