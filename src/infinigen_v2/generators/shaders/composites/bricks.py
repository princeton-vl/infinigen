def brick_color_pattern_distribution(
    rng: pf.RNG,
    vector: pf.ProcNode[pf.Vector],
    brick_id: pf.ProcNode[pf.Vector],
    brick_random: pf.ProcNode[pf.Color],
):
    def selector_option(rs, vector, brick_id, **_):
        rs1, rs2, rs3 = rs.spawn(3)
        fac = brick_selector.brick_selector_distribution(rng=rs1, brick_id=brick_id)
        color_1 = brick_concrete.brick_concrete_color_distribution(rs2)
        color_2 = brick_concrete.brick_concrete_color_distribution(rs3)
        return pf.nodes.color.mix_rgb(
            factor=fac.selection,
            a=color_1,
            b=color_2,
        )

    def mix_per_brick_option(ro, vector, brick_random, **_):
        ro1, ro2 = ro.spawn(2)
        return pf.nodes.color.mix_rgb(
            factor=brick_random.x,
            a=brick_concrete.brick_concrete_color_distribution(ro1),
            b=brick_concrete.brick_concrete_color_distribution(ro2),
            blend_type="SOFT_LIGHT",
        )

    def discrete_per_brick_option(ro, vector, brick_random, **_):
        ratio = pf.random.uniform(ro, 0.1, 0.9)
        return pf.nodes.color.mix_rgb(
            factor=brick_random.x > ratio,
            a=brick_concrete.brick_concrete_color_distribution(ro),
            b=brick_concrete.brick_concrete_color_distribution(ro),
            blend_type="SOFT_LIGHT",
        )

    def constant_option(rc, **_):
        return brick_concrete.brick_concrete_color_distribution(rc)

    def tile_pattern_color_option(rt, brick_id, **_):
        rt2, rt3, rt4 = rt.spawn(3)
        # x/y scales normalize brick_id to metric space for ~1.5m tile repeat
        tile_vector = pf.nodes.math.combine_xyz(
            x=brick_id.x * 0.667, y=brick_id.y * 0.233
        )
        border = pf.random.uniform(rt2, 0.15, 0.25)
        tile_mask = tile_mask_distribution(rt2, vector=tile_vector, border=border)
        color_1 = brick_concrete.brick_concrete_color_distribution(rt3)
        color_2 = brick_concrete.brick_concrete_color_distribution(rt4)
        tile_type = tile_mask.result.astype(dtype=pf.Vector).x > 0.5
        return pf.nodes.color.mix_rgb(factor=tile_type, a=color_1, b=color_2)

    r1, r2 = rng.spawn(2)

    color_func = pf.control.choice(
        r1,
        [
            (selector_option, 2.0),  # has more diversity than the others, upweight it
            (constant_option, 1.0),
            (mix_per_brick_option, 1.0),
            (discrete_per_brick_option, 1.0),
            (tile_pattern_color_option, 2.0),
            # random_per_brick
        ],
    )
    return color_func(r2, vector=vector, brick_id=brick_id, brick_random=brick_random)


def brick_shader_composite_distribution(
    rng: pf.RNG,
    vector: pf.ProcNode[pf.Vector],
    brick_id: pf.ProcNode[pf.Vector],
    brick_random: pf.ProcNode[pf.Color],
) -> pf.Material:
    """
    Chooses a brick material, and when possible chooses a per-brick color scheme, e.g random, checkered, striped, etc
    """

    with_color = pf.control.choice(
        rng,
        [
            # TODO: more materials with the `color` argument
            (brick_concrete.brick_concrete_distribution, 2.0),
        ],
    )

    no_color = pf.control.choice(
        rng,
        [
            # TODO: more materials with no `color` argument
            (granite.granite_distribution, 1.0),
            (stone_smooth.stone_smooth_distribution, 0.5),
            (concrete.concrete_distribution, 1.0),
        ],
    )

    color_pattern = brick_color_pattern_distribution(
        rng, vector, brick_id, brick_random
    )
    mat_with_pattern = with_color(rng, vector, color=color_pattern)
    mat_no_pattern = no_color(rng, vector)
    return pf.control.choice(
        rng,
        [
            (mat_with_pattern, 2.0),
            (mat_no_pattern, 1.0),
        ],
    )


@pf.tracer.grammar
def bricks_distribution(
    rng: pf.RNG,
    vector: pf.ProcNode[pf.Vector],
    brick_cutter: BrickCutterResult | None = None,
    brick_shader: pf.Material | None = None,
    grout_shader: pf.Material | None = None,
) -> pf.Material:
    r1, r2, r3 = rng.spawn(3)

    if brick_cutter is None:
        brick_cutter = brick_cutter_distribution(r1, vector)

    if brick_shader is None:
        brick_shader = brick_shader_composite_distribution(
            r2,
            vector=brick_cutter.vector,
            brick_id=brick_cutter.brick_id,
            brick_random=brick_cutter.random,
        )

    if grout_shader is None:
        grout_shader = brick_concrete.brick_concrete_grout_distribution(r3, vector)

    surface = pf.nodes.shader.mix_shader(
        factor=brick_cutter.mask,
        a=grout_shader.surface,
        b=brick_shader.surface,
    )
    displacement = pf.nodes.math.mix(
        factor=brick_cutter.mask,
        a=grout_shader.displacement,
        b=brick_shader.displacement,
    )

    cutter_displacement = pf.nodes.shader.displacement(
        height=brick_cutter.height,
        midlevel=0.0,
        normal=(0.0, 0.0, 0.0),
    )
    displacement = displacement + cutter_displacement
    return pf.Material(
        surface=surface,
        displacement=displacement,
    )


def brick_concrete_pristine(rng: pf.RNG, vector: pf.ProcNode[pf.Vector]) -> pf.Material:
    r1, r2 = rng.spawn(2)
    displacement = pf.random.uniform(r1, 0.1, 0.2)
    return brick_concrete.brick_concrete_distribution(
        r2, vector, displacement_pct=displacement
    )


def bricks_pristine_distribution(
    rng: pf.RNG,
    vector: pf.ProcNode[pf.Vector],
) -> pf.Material:
    r1, r2, r3, r4 = rng.spawn(4)

    shader = pf.control.choice(
        r1,
        [
            (brick_concrete_pristine, 0.5),
            (concrete.concrete_distribution, 0.5),
            (granite.granite_smooth_distribution, 1.0),
            (marble.marble_distribution, 1.5),
        ],
    )

    dimensions = brick_dimensions_distribution(r1)
    dimensions = pf.nodes.math.vector_multiply(dimensions, (1, 2, 1))

    cutter = brick_cutter_distribution(
        r2, vector, irregularity=0, warp_strength=pf.random.uniform(rng, 0.05, 0.3)
    )

    return bricks_distribution(
        r3,
        vector,
        brick_cutter=cutter,
        brick_shader=shader(r2, cutter.vector),
        grout_shader=brick_concrete.brick_concrete_grout_distribution(r4, vector),
    )


def bricks_masonry_distribution(
    rng: pf.RNG,
    vector: pf.ProcNode[pf.Vector],
    brick_shader: pf.Material | None = None,
    grout_shader: pf.Material | None = None,
) -> pf.Material:
    """

    Args:
    ---
    """

    rng, r1, r2, r3, r4 = rng.spawn(5)

    r1_1, r1_2 = r1.spawn(2)
    dimensions = brick_dimensions_distribution(
        r1_1,
        brick_width_mean=0.35,
        brick_aspect_mean=0.5,
    )
    brick_cutter = brick_cutter_distribution(
        r1,
        vector,
        brick_dimensions=dimensions,
        gab=pf.random.uniform(rng, 0.025, 0.05),
    )

    if brick_shader is None:
        r2_1, r2_2 = r2.spawn(2)
        distrib = pf.control.choice(
            r2_1,
            [
                (brick_concrete.brick_concrete_distribution, 0.2),
                (granite.granite_distribution, 2.0),
                (marble.marble_distribution, 0.5),
            ],
        )
        brick_shader = distrib(rng=r2_2, vector=vector)

    if grout_shader is None:
        # artist demos never used light-colored grout for masonry
        color_hsv = pf.random.uniform(
            rng,
            np.array([0.0, 0.11, 0.06]),
            np.array([0.05, 0.56, 0.14]),
        )
        grout_color = pf.color.hsv_to_rgba(color_hsv)

        grout_shader = brick_concrete.brick_concrete_grout_distribution(
            r3, vector, color=grout_color
        )

    surface = pf.nodes.shader.mix_shader(
        factor=brick_cutter.mask,
        a=grout_shader.surface,
        b=brick_shader.surface,
    )
    displacement = pf.nodes.math.mix(
        factor=brick_cutter.mask,
        a=grout_shader.displacement,
        b=brick_shader.displacement,
    )

    displacement_layer_2_mask = pf.nodes.math.map_range(
        value=brick_cutter.distance_from_edge, from_max=0.43
    )

    dimensions_sep = pf.nodes.math.separate_xyz(vector=dimensions)

    mult = dimensions_sep.x * (3 * pf.random.clip_gaussian(rng, 1, 0.2, 0.7, 1.7))
    # exp = pf.random.uniform(rng, 0.3, 1)
    height = mult * (brick_cutter.distance_from_edge)  # **exp)
    clip = dimensions_sep.x * pf.random.uniform(rng, 0.03, 0.17)

    masonry_displacement_result = masonry.masonry_displacement_distribution(
        rng=r4,
        vector=brick_cutter.vector,
        layer_1_height=height,
        layer_1_clamp_max=clip,
        layer_2_mask=displacement_layer_2_mask,
        layer_2_height=dimensions_sep.y,
    )

    displacement = masonry_displacement_result.displacement
    return pf.Material(
        surface=surface,
        displacement=displacement,
    )


