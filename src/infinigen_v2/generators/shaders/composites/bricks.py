def bricks_paint_distribution(
    rng: pf.RNG,
    vector: pf.ProcNode[pf.Vector],
    bricks: pf.Material | None = None,
    paint_material: pf.Material | None = None,
    brick_cutter: BrickCutterResult | None = None,
    mask: pf.ProcNode[float] | None = None,
) -> pf.Material:
    """
    Creates a material with bricks covered with paint, sometimes with cracks allowing the underlying bricks to show through

    Note: you could also just use `bricks_distribution` then `paint_overlay_distribution`, but this would miss a couple features:
    - some options for the brick material are large enough to cause `Cycles: out of SVM stack space` when using paint+mask
    - this function ensures that cracks in paint

    """

    r1, r2, r3, r4, r5 = rng.spawn(5)

    if brick_cutter is None:
        brick_cutter = brick_cutter_distribution(
            r1,
            vector,
            grout_extrusion=0,
        )

    if bricks is None:
        bricks = brick_concrete.brick_concrete_distribution(r2, brick_cutter.vector)

    if paint_material is None:
        # NOTE: using `vector` not `brick_cutter_result.vector`
        #   this means paint strokes are contigous between bricks
        #   (which is realistic unless they are painted BEFORE assembly)
        paint_material = paint.paint_distribution(r3, vector=vector)

    cutter_displacement = pf.nodes.shader.displacement(
        height=brick_cutter.height,
        midlevel=0.0,
        normal=(0.0, 0.0, 0.0),
    )
    bricks_displacement = bricks.displacement + cutter_displacement
    bricks_and_paint_displacement = bricks_displacement + paint_material.displacement

    if mask is not None:
        mix_mask = mask
    else:
        mix_mask = cracks.cracks_distribution(
            r4,
            vector,
            displacement_a=bricks_and_paint_displacement,
            displacement_b=paint_material.displacement,
            height_threshold=0.02,
        )

    cracks_mix_surface = pf.nodes.shader.mix_shader(
        factor=mix_mask,
        a=bricks.surface,
        b=paint_material.surface,
    )
    cracks_mix_displacement = pf.nodes.math.mix(
        factor=mix_mask,
        a=bricks_displacement,
        b=bricks_and_paint_displacement,
    )

    paint_overlay_cracks = pf.Material(
        surface=cracks_mix_surface,  # cracks_mix_surface,
        displacement=cracks_mix_displacement,
    )
    paint_overlay_nomask = pf.Material(
        surface=paint_material.surface,
        displacement=bricks_and_paint_displacement,
    )

    if mask is not None:
        # the user provided a mask, so we have to mix it
        return paint_overlay_cracks
    else:
        # OPTIMIZATION: in the case where THIS FUNCTION chooses to use no mask,
        #   we can avoid using `cracks_mix_surface`, which results in a smaller/cheaper material
        return pf.control.choice(
            r5, [(paint_overlay_cracks, 1.0), (paint_overlay_nomask, 1.0)]
        )
