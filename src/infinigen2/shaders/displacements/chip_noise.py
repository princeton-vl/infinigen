    numerator_6 = pf.nodes.math.clamp(detail - 3.0)
    numerator_w_1 = 40.0 * size
    numerator_scale_1 = numerator_scale_2 * lacunarity
    numerator_5 = pf.nodes.texture.voronoi_distance(
        vector=vector,
        w=w + numerator_w_1,
        scale=numerator_scale_4 * numerator_scale_1,
        normalize=True,
        voronoi_dimensions="4D",
    )
    denominator_5 = (denominator_6 * denominator_7) * denominator_7
    numerator_4 = numerator_5 * denominator_5
    numerator_3 = (numerator_10 + numerator_7) + (numerator_6 * numerator_4)
    numerator_2 = pf.nodes.math.clamp(detail - 4.0)
    numerator_w = 50.0 * size
    numerator_scale = numerator_scale_1 * lacunarity
    numerator_1 = pf.nodes.texture.voronoi_distance(
        vector=vector,
        w=w + numerator_w,
        scale=numerator_scale_4 * numerator_scale,
        normalize=True,
        voronoi_dimensions="4D",
    )
    numerator = numerator_2 * numerator_1 * (denominator_5 * denominator_7)
    denominator_4 = numerator_16 * denominator_7
    denominator_3 = (denominator_4 + 1.0) + (denominator_6 * numerator_13)
    denominator_2 = (denominator_6 * denominator_7) * numerator_9
    denominator_1 = (denominator_3 + denominator_2) + (denominator_5 * numerator_6)
    denominator = (denominator_5 * denominator_7) * numerator_2

    divide = (numerator_3 + numerator) / (denominator_1 + denominator)
    return divide
