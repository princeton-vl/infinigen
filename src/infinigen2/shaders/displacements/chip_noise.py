    denominator_7 = 1.0 / lacunarity
    numerator_14 = numerator_16 * (numerator_15 * denominator_7)
    numerator_13 = pf.nodes.math.clamp(detail - 1.0)
    numerator_w_3 = 20.0 * size
    numerator_scale_3 = lacunarity**2.0
    numerator_12 = pf.nodes.texture.voronoi_distance(
        vector=vector,
        w=w + numerator_w_3,
        scale=numerator_scale_4 * numerator_scale_3,
        normalize=True,
        voronoi_dimensions="4D",
    )
    denominator_6 = denominator_7**2.0
    numerator_11 = numerator_12 * denominator_6
    numerator_10 = (numerator_17 + numerator_14) + (numerator_13 * numerator_11)
    numerator_9 = pf.nodes.math.clamp(detail - 2.0)
    numerator_w_2 = 30.0 * size
    numerator_scale_2 = numerator_scale_3 * lacunarity
