def metal_any_color_rand(rng) -> pf.Color:
    hue = pf.random.uniform(rng, 0.03, 0.12)
    value = pf.random.clip_gaussian(rng, 0.3, 0.5, 0.03, 0.95)

    # Prevent saturation from being too high if value is low
    max_saturation = max(0, min(1, 5 / 3 * (value - 0.05)))
    saturation = pf.random.clip_gaussian(
        rng, max_saturation / 2.2, max_saturation / 4, 0.0, max_saturation
    )

    return pf.color.hsv_color(hue=hue, saturation=saturation, value=value)


def metal_brushed_linear_rand(
    rng: pf.RNG,
    vector: pf.ProcNode[pf.Vector],
    base_color: pf.Color | None = None,
) -> pf.Material:
    r1, r2 = rng.spawn(2)

    if base_color is None:
        base_color = metal_any_color_rand(r1)

    # Placeholder parameters (to be filled later)
    brush_type = pf.random.uniform(r2, 0.0, 0.05)
    brush_size = pf.random.clip_gaussian(r2, 3, 3, 0.0, 8.5)
    stretch_factor = pf.random.uniform(r2, 0.5727, 0.9)
    brush_rotation = (np.pi / 2, 0.0, np.pi / 2)  # (0.0, np.pi/2, 0.0)
    color = base_color
    color_variation = pf.random.uniform(r2, 0.2333, 0.9)
    max_roughness = 0.6
    roughness = pf.random.uniform(r2, 0.2, max_roughness)
    anisotropy = (
        pf.random.uniform(r2, 0.1, 0.6) * (1 - roughness / max_roughness)
        + roughness / max_roughness * 0.1
    )
    anisotropy_rotation = pf.random.uniform(r2, 0.0, 1.0)
    tangent = (0, 0, 0)
    height_scale = pf.random.clip_gaussian(r2, 2.0, 4.0, 0.0, 10.0)
    height_offset = pf.random.uniform(r2, -5.0, 5.0)

    metal_brushed_result = metal_brushed(
        vector=vector,
        brush_type=brush_type,
        brush_size=brush_size,
        stretch_factor=stretch_factor,
        brush_rotation=brush_rotation,
        color=color,
        color_variation=color_variation,
        roughness=roughness,
        anisotropy=anisotropy,
        anisotropy_rotation=anisotropy_rotation,
        tangent=tangent,
        height_scale=height_scale,
        height_offset=height_offset,
    )

    return metal_brushed_result


def metal_brushed_radial_rand(
    rng: pf.RNG,
    vector: pf.ProcNode[pf.Vector],
    base_color: pf.Color | None = None,
) -> pf.Material:
    r1, _ = rng.spawn(2)

    if base_color is None:
        base_color = metal_any_color_rand(r1)

    color = base_color

    return pf.control.choice(
        rng,
        [
            (
                metal_brushed(
                    vector=vector,
                    brush_type=2.0,
                    brush_size=0.5,
                    stretch_factor=0.8897,
                    color=color,
                    roughness=0.1821,
                    anisotropy=0.5,
                    height_scale=3.0,
                ),
                1.0,
            ),
            (
                metal_brushed(
                    vector=vector,
                    brush_type=1.0,
                    stretch_factor=0.8,
                    color=color,
                    roughness=0.2825,
                    anisotropy=0.5,
                ),
                1.0,
            ),
        ],
    )
