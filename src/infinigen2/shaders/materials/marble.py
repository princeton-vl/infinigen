@pf.nodes.node_function
def _organic_voronoi(
    vector: t.SocketOrVal[pf.Vector],
    w: t.SocketOrVal[float],
    scale: t.SocketOrVal[float],
) -> pf.ProcNode[float]:
    # Code generated by procfunc v0.18.0-ifg
    voronoi = pf.nodes.texture.voronoi(
        vector=vector,
        scale=scale,
        lacunarity=2.5,
        normalize=True,
        voronoi_dimensions="4D",
        w=w,
    )

    voronoi_smooth_f1 = pf.nodes.texture.voronoi_smooth_f1(
        vector=vector,
        scale=scale,
        lacunarity=2.5,
        smoothness=1.0,
        normalize=True,
        voronoi_dimensions="4D",
        w=w,
    )
    numerator = pf.nodes.math.clamp(voronoi.distance - voronoi_smooth_f1.distance)

    divide = numerator / 0.17

    return divide


class _OrganicGrainResult(NamedTuple):
    vector: t.SocketOrVal[pf.Vector]
    grain_map: pf.ProcNode[float]


@pf.nodes.node_function
def _organic_grain(
    vector: t.SocketOrVal[pf.Vector],
    grain_size: t.SocketOrVal[float],
    grain_stretch: t.SocketOrVal[float],
    grain_detail: t.SocketOrVal[float],
    grain_lacunarity: t.SocketOrVal[float],
    seed: t.SocketOrVal[float],
) -> _OrganicGrainResult:
    # Code generated by procfunc v0.18.0-ifg
    grain_map_factor_1 = pf.nodes.math.clamp(grain_detail)

    organic_voronoi_scale = (1.0 / grain_size) * 10.0

    organic_voronoi_result = _organic_voronoi(
        vector=vector, w=seed, scale=organic_voronoi_scale
    )
    grain_map_factor_from_min = pf.nodes.math.constant(0.31)

    grain_map_factor_from_max = grain_map_factor_from_min + 0.1
    grain_map_factor_0 = pf.nodes.math.map_range(
        value=organic_voronoi_result,
        from_max=grain_map_factor_from_max,
        from_min=grain_map_factor_from_min,
    )
    grain_map_b_factor_1 = pf.nodes.math.clamp(grain_detail - 1.0)
    organic_scale_1 = organic_voronoi_scale * grain_lacunarity

    organic_voronoi_result_1 = _organic_voronoi(
        vector=vector, w=seed, scale=organic_scale_1
    )
    grain_map_b_factor_0 = pf.nodes.math.map_range(
        value=organic_voronoi_result_1,
        from_max=grain_map_factor_from_max,
        from_min=grain_map_factor_from_min,
    )

    grain_map_a = pf.nodes.math.clamp(grain_detail - 2.0)
    organic_scale_0 = organic_scale_1 * grain_lacunarity

    organic_voronoi_result_2 = _organic_voronoi(
        vector=vector, w=seed, scale=organic_scale_0
    )
    grain_map_b_1 = pf.nodes.math.map_range(
        value=organic_voronoi_result_2,
        from_max=grain_map_factor_from_max,
        from_min=grain_map_factor_from_min,
    )

    organic_voronoi_result_3 = _organic_voronoi(
        vector=vector, w=seed, scale=organic_scale_0 * grain_lacunarity
    )

    grain_map_b_b = pf.nodes.math.mix(
        a=organic_voronoi_result_2,
        b=organic_voronoi_result_3,
        factor=grain_map_a * grain_map_b_1,
    )

    grain_map_b_0 = pf.nodes.math.mix(
        a=organic_voronoi_result_1,
        b=grain_map_b_b,
        factor=grain_map_b_factor_1 * grain_map_b_factor_0,
    )
    grain_map = pf.nodes.math.mix(
        a=organic_voronoi_result,
        b=grain_map_b_0,
        factor=grain_map_factor_1 * grain_map_factor_0,
    )
    return _OrganicGrainResult(vector, grain_map)


class _ChipGrainResult(NamedTuple):
    vector: t.SocketOrVal[pf.Vector]
    grain_map: pf.ProcNode[float]


@pf.nodes.node_function
def _chip_grain(
    vector: t.SocketOrVal[pf.Vector],
    grain_size: t.SocketOrVal[float],
    grain_stretch: t.SocketOrVal[float],
    grain_detail: t.SocketOrVal[float],
    grain_lacunarity: t.SocketOrVal[float],
    seed: t.SocketOrVal[float],
) -> _ChipGrainResult:
    # Code generated by procfunc v0.18.0-ifg
    voronoi_distance_vector_x = 1.0 / grain_stretch

    voronoi_distance_vector = pf.nodes.math.combine_xyz(
        x=voronoi_distance_vector_x,
        y=voronoi_distance_vector_x,
        z=1.0,
    )
    voronoi_distance_scale = 1.0 / grain_size
    voronoi_distance = pf.nodes.texture.voronoi_distance(
        vector=vector * voronoi_distance_vector,
        scale=voronoi_distance_scale * 10.0,
        detail=grain_detail,
        lacunarity=grain_lacunarity,
        normalize=True,
        voronoi_dimensions="4D",
        w=seed,
    )
    grain_map = 1.0 - (voronoi_distance * 2.0)

    return _ChipGrainResult(vector, grain_map)


