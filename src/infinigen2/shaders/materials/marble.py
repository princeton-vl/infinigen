class _GrainProcessResult(NamedTuple):
    grain: pf.ProcNode[float]
    raw_grain: pf.ProcNode[float]
    edge_mask: pf.ProcNode[float]


@pf.nodes.node_function
def _grain_process(
    grain_spread: t.SocketOrVal[float],
    grain_smoothness: t.SocketOrVal[float],
    invert: t.SocketOrVal[float],
    grain: t.SocketOrVal[float],
) -> _GrainProcessResult:
    # Code generated by procfunc v0.18.0-ifg
    raw_grain = pf.nodes.math.mix(a=grain, b=1.0 - grain, factor=invert)

    grain_from_min_b = pf.nodes.math.clamp(grain_spread)

    grain_from_min = 1.0 - grain_from_min_b
    grain_a = pf.nodes.math.clamp(grain_smoothness)
    grain_from_max_b_b = pf.nodes.math.maximum(a=grain_a, b=0.0001)
    grain_from_max_b = grain_from_min_b * grain_from_max_b_b
    grain_from_max = grain_from_min + grain_from_max_b
    grain_1 = pf.nodes.math.map_range(
        value=raw_grain,
        from_max=grain_from_max,
        from_min=grain_from_min,
    )
    edge_mask = pf.nodes.math.map_range(
        value=raw_grain,
        from_max=grain_from_max + grain_from_max_b,
        from_min=grain_from_max,
    )

    return _GrainProcessResult(grain_1, raw_grain, edge_mask)


class GrainToShaderResult(NamedTuple):
    bsdf: pf.ProcNode[pf.Shader]
    color: t.SocketOrVal[pf.Color]
    displacement: pf.ProcNode[pf.Vector]


@pf.nodes.node_function
def grain_to_shader(
    color: t.SocketOrVal[pf.Color],
    grain: t.SocketOrVal[float],
    roughness: t.SocketOrVal[float],
    specularity: t.SocketOrVal[float],
    height: t.SocketOrVal[float],
) -> GrainToShaderResult:
    # Code generated by procfunc v0.18.0-ifg
    bsdf_b_1 = roughness * 0.07

    bsdf_roughness_to_min = pf.nodes.math.clamp(roughness + bsdf_b_1)
    bsdf_roughness_to_max = pf.nodes.math.clamp(roughness - bsdf_b_1)
    bsdf_roughness = pf.nodes.math.map_range(
        value=grain,
        from_min=0.25,
        to_max=bsdf_roughness_to_max,
        to_min=bsdf_roughness_to_min,
    )
    bsdf_b_0 = specularity * 0.07
    bsdf_specular_ior_level_to_min = pf.nodes.math.clamp(specularity - bsdf_b_0)
    bsdf_specular_ior_level_to_max = pf.nodes.math.clamp(specularity + bsdf_b_0)
    bsdf_specular_ior_level = pf.nodes.math.map_range(
        value=grain,
        from_min=0.25,
        to_max=bsdf_specular_ior_level_to_max,
        to_min=bsdf_specular_ior_level_to_min,
    )
    principled = pf.nodes.shader.principled_bsdf(
        base_color=color,
        roughness=bsdf_roughness,
        ior=1.45,
        subsurface_anisotropy=0.0,
        specular_ior_level=bsdf_specular_ior_level,
    )

    displacement = pf.nodes.shader.displacement(
        height=grain,
        midlevel=0.0,
        scale=height,
        normal=(0.0, 0.0, 0.0),
    )

    return GrainToShaderResult(principled, color, displacement)


class GrainLayer001Result(NamedTuple):
    vector: t.SocketOrVal[pf.Vector]
    color: pf.ProcNode[pf.Color]
    grain: pf.ProcNode[float]
    layer_count: pf.ProcNode[float]


@pf.nodes.node_function
def grain_layer(
    vector: t.SocketOrVal[pf.Vector],
    base_color: t.SocketOrVal[pf.Color],
    grain: t.SocketOrVal[float],
    layer_count: t.SocketOrVal[float],
    opacity: t.SocketOrVal[float],
    grain_type: t.SocketOrVal[float],
    grain_color: t.SocketOrVal[pf.Color],
    mix_mode: t.SocketOrVal[float],
    grain_size: t.SocketOrVal[float],
    grain_stretch: t.SocketOrVal[float],
    grain_spread: t.SocketOrVal[float],
    random_spread: t.SocketOrVal[float],
    grain_smoothness: t.SocketOrVal[float],
    grain_rotation: t.SocketOrVal[pf.Vector],
    distortion_strength: t.SocketOrVal[float],
    distortion_size: t.SocketOrVal[float],
    distortion_details: t.SocketOrVal[float],
    seed: t.SocketOrVal[float],
    invert: t.SocketOrVal[float],
) -> GrainLayer001Result:
    # Code generated by procfunc v0.18.0-ifg
    color_factor = pf.nodes.math.floor(mix_mode)

    chip_x = pf.nodes.math.clamp(1.0 / grain_stretch)

    chip_grain_vector_a = pf.nodes.math.combine_xyz(x=chip_x, y=chip_x, z=1.0)
    chip_b = pf.nodes.math.vector_rotate_euler(
        vector=vector, rotation=grain_rotation, center=(0, 0, 0)
    )
    chip_a_scale_0 = grain_size * distortion_size
    noise = pf.nodes.texture.noise(
        vector=chip_b,
        scale=1.0 / chip_a_scale_0,
        detail=distortion_details,
        lacunarity=2.5,
        noise_dimensions="4D",
        w=seed,
    )

    chip_a_1 = pf.nodes.math.vector_scale(
        vector=noise.color.astype(dtype=pf.Vector) - (0.5, 0.5, 0.5),
        scale=distortion_strength * chip_a_scale_0,
    )

    chip_grain_vector = chip_grain_vector_a * (chip_a_1 + chip_b)
    noise_1_scale = 1.0 / grain_size

    camera_data = pf.nodes.shader.camera_data()

    chip_a_value = 1.0 / (camera_data.view_distance / 3.0)

    chip_a_0 = pf.nodes.math.logarithm(value=grain_size * chip_a_value, base=2.5)
    noise_1_detail = pf.nodes.math.minimum(a=chip_a_0 + 2.0, b=16.0)

    noise_1 = pf.nodes.texture.noise(
        vector=chip_grain_vector,
        scale=noise_1_scale * 19.099998,
        detail=noise_1_detail,
        lacunarity=2.5,
    )
    color_b_b_b = pf.nodes.math.map_range(
        value=noise_1.color.astype(dtype=pf.Vector),
        from_max=(0.75, 0.75, 0.75),
        from_min=(0.25, 0.25, 0.25),
        to_max=(1.0, 1.0, 1.0),
        to_min=(0.0, 0.0, 0.0),
    )

    color_b_b_0 = pf.nodes.math.mix(
        a=(0.0, 1.0, 1.0), b=color_b_b_b, factor=random_spread
    )
    color_b_1 = 1.0 - color_b_b_0.x
    grain_factor = pf.nodes.math.floor(grain_type)

    chip_grain_grain_detail = pf.nodes.math.minimum(a=chip_a_0 + 3.0, b=16.0)

    noise_grain_result = _noise_grain(
        vector=chip_grain_vector,
        grain_size=grain_size,
        grain_stretch=grain_stretch,
        grain_detail=chip_grain_grain_detail,
        grain_lacunarity=2.5,
        seed=seed,
    )

    sharp_noise_grain_result = _sharp_noise_grain(
        vector=chip_grain_vector,
        grain_size=grain_size,
        grain_stretch=grain_stretch,
        grain_detail=chip_grain_grain_detail,
        grain_lacunarity=2.5,
        seed=seed,
    )

    grain_a_a_a = pf.nodes.math.mix(
        a=noise_grain_result.grain_map,
        b=sharp_noise_grain_result.grain_map,
        factor=grain_factor > 1.0,
    )

    voronoi_grain_result = _voronoi_grain(
        vector=chip_grain_vector,
        grain_size=grain_size,
        grain_stretch=grain_stretch,
        grain_detail=chip_grain_grain_detail,
        grain_lacunarity=2.5,
        seed=seed,
    )

    grain_a_a = pf.nodes.math.mix(
        a=grain_a_a_a,
        b=voronoi_grain_result.grain_map,
        factor=grain_factor > 2.0,
    )

    chip_grain_result = _chip_grain(
        vector=chip_grain_vector,
        grain_size=grain_size,
        grain_stretch=grain_stretch,
        grain_detail=chip_grain_grain_detail,
        grain_lacunarity=2.5,
        seed=seed,
    )

    grain_a = pf.nodes.math.mix(
        a=grain_a_a,
        b=chip_grain_result.grain_map,
        factor=grain_factor > 3.0,
    )

    organic_grain_result = _organic_grain(
        vector=chip_grain_vector,
        grain_size=grain_size,
        grain_stretch=grain_stretch,
        grain_detail=chip_grain_grain_detail,
        grain_lacunarity=2.5,
        seed=seed,
    )

    grain_b = pf.nodes.math.float_curve(
        value=organic_grain_result.grain_map,
        curve=np.array([[0.0, 0.0], [0.1318, 0.3687], [0.5636, 0.8438], [1.0, 1.0]]),
        factor=1.0,
    )

    grain_1 = pf.nodes.math.mix(a=grain_a, b=grain_b, factor=grain_factor > 4.0)
    grain_process_result = _grain_process(
        grain_spread=grain_spread - color_b_b_0.x,
        grain_smoothness=color_b_b_0.y * grain_smoothness,
        invert=invert,
        grain=grain_1,
    )
    gamma = pf.nodes.color.gamma(
        color=grain_process_result.grain.astype(dtype=pf.Color),
        gamma=2.2,
    )

    color_b_factor = (opacity * color_b_1) * gamma.astype(dtype=float)

    color_a_a = pf.nodes.color.mix_rgb(
        factor=color_b_factor, a=base_color, b=grain_color
    )
    color_a_b = pf.nodes.color.mix_rgb(
        factor=color_b_factor,
        a=base_color,
        b=grain_color,
        blend_type="MULTIPLY",
    )
    color_a = pf.nodes.color.mix_rgb(
        factor=color_factor > 1.0, a=color_a_a, b=color_a_b
    )
    color_b_0 = pf.nodes.color.mix_rgb(
        factor=color_b_factor,
        a=base_color,
        b=grain_color,
        blend_type="SOFT_LIGHT",
    )
    color = pf.nodes.color.mix_rgb(factor=color_factor > 2.0, a=color_a, b=color_b_0)
    layer_count_1 = layer_count + opacity

    return GrainLayer001Result(vector, color, grain_1, layer_count_1)


