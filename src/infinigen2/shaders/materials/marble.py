@pf.nodes.node_function
def _sharp_noise_grain(
    vector: t.SocketOrVal[pf.Vector],
    grain_size: t.SocketOrVal[float],
    grain_stretch: t.SocketOrVal[float],
    grain_detail: t.SocketOrVal[float],
    grain_lacunarity: t.SocketOrVal[float],
    seed: t.SocketOrVal[float],
) -> _SharpNoiseGrainResult:
    # Code generated by procfunc v0.18.0-ifg
    noise_vector_x = 1.0 / grain_stretch

    noise_vector = pf.nodes.math.combine_xyz(x=noise_vector_x, y=noise_vector_x, z=1.0)
    noise_scale = 1.0 / grain_size
    noise = pf.nodes.texture.noise(
        vector=vector * noise_vector,
        scale=noise_scale * 10.0,
        detail=grain_detail,
        lacunarity=grain_lacunarity,
        noise_dimensions="4D",
        normalize=False,
        w=seed,
    )
    grain_map_b = pf.nodes.math.absolute(noise.fac)

    grain_map = 1.0 - grain_map_b
    return _SharpNoiseGrainResult(vector, grain_map)


