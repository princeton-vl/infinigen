def wood_color_rand(rng: pf.RNG):
    """Sample HSV colors biased toward the wood_grain_* preset cluster.

    Means/stds reflect the modal preset values; clip ranges admit the
    rings_* / stained / varnished outliers, but rarely. c1 and c2 share a
    hue with a small jitter (presets show 0-0.04 offset between them).
    """
    hue = pf.random.clip_gaussian(rng, 0.07, 0.03, 0.02, 0.12)
    hue_offset = pf.random.clip_gaussian(rng, 0.0, 0.015, -0.04, 0.04)
    color_1 = pf.color.hsv_color(
        hue=hue,
        saturation=pf.random.clip_gaussian(rng, 0.70, 0.20, 0.20, 0.95),
        value=pf.random.clip_gaussian(rng, 0.10, 0.10, 0.02, 0.45),
    )
    color_2 = pf.color.hsv_color(
        hue=hue + hue_offset,
        saturation=pf.random.clip_gaussian(rng, 0.75, 0.20, 0.20, 0.95),
        value=pf.random.clip_gaussian(rng, 0.32, 0.20, 0.18, 0.85),
    )
    flake_color = pf.color.hsv_color(
        hue=pf.random.clip_gaussian(rng, 0.05, 0.06, 0.0, 0.50),
        saturation=pf.random.clip_gaussian(rng, 0.60, 0.20, 0.0, 0.75),
        value=pf.random.clip_gaussian(rng, 0.10, 0.06, 0.05, 0.27),
    )
    fiber_color = pf.color.hsv_color(
        hue=pf.random.clip_gaussian(rng, 0.05, 0.01, 0.03, 0.06),
        saturation=pf.random.clip_gaussian(rng, 0.78, 0.10, 0.45, 0.85),
        value=pf.random.clip_gaussian(rng, 0.07, 0.03, 0.05, 0.12),
    )
    knot_color = pf.color.hsv_color(
        hue=pf.random.clip_gaussian(rng, 0.05, 0.01, 0.03, 0.07),
        saturation=pf.random.clip_gaussian(rng, 0.65, 0.10, 0.35, 0.70),
        value=pf.random.clip_gaussian(rng, 0.05, 0.025, 0.025, 0.10),
    )
    return color_1, color_2, flake_color, fiber_color, knot_color


