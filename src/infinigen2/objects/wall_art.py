from typing import NamedTuple

import procfunc as pf
from procfunc.nodes import types as t

from infinigen2.shaders.functionality_lists import (
    art_pattern_material_rand,
    furniture_material_rand,
    mirror_material_rand,
)
from infinigen2.shaders.materials import (
    terrazzo,
)

__all__ = [
    "WallArtResult",
    "art_frame",
    "mirror_rand",
    "mirror_surface_material_rand",
    "wall_art_rand",
]


class WallArtResult(NamedTuple):
    mesh: pf.MeshObject


@pf.nodes.node_function
def art_frame(
    dimensions: t.SocketOrVal[pf.Vector],
    frame_width: t.SocketOrVal[float],
    frame_material: t.SocketOrVal[pf.Material],
    panel_material: t.SocketOrVal[pf.Material],
    panel_depth_pct: t.SocketOrVal[float],
) -> pf.ProcNode:
    # Code generated by procfunc v0.18.0-ifg

    grid_size_x = pf.nodes.math.multiply_add(a=frame_width, b=-2.0, addend=dimensions.y)
    grid_size_y = pf.nodes.math.multiply_add(a=frame_width, b=-2.0, addend=dimensions.z)
    grid = pf.nodes.geo.mesh_grid(
        size_x=grid_size_x,
        size_y=grid_size_y,
        vertices_x=2,
        vertices_y=2,
    )

    extrude_value = pf.nodes.math.combine_xyz(x=grid_size_x, y=grid_size_y)

    store_named_attribute = pf.nodes.geo.store_named_attribute(
        geometry=grid.mesh,
        name="UVMap",
        value=extrude_value * grid.uv_map,
        domain="CORNER",
        data_type="FLOAT2",
    )

    extrude = pf.nodes.geo.extrude_mesh(
        mesh=store_named_attribute,
        offset_scale=dimensions.x * panel_depth_pct,
    )

    set_material = pf.nodes.geo.set_material(
        geometry=extrude.mesh,
        material=panel_material,
        selection=True,
    )

    join_geometries_1_switch = pf.nodes.func.greater_than(a=frame_width, b=0.0)

    curve_quadrilateral = pf.nodes.geo.curve_quadrilateral(
        width=grid_size_x, height=grid_size_y
    )
    curve_line_start = pf.nodes.math.combine_xyz(frame_width)
    curve_line = pf.nodes.geo.curve_line(start=curve_line_start, end=(0.0, 0.0, 0.0))
    curve_to = pf.nodes.geo.curve_to_mesh(
        curve=curve_quadrilateral, profile_curve=curve_line
    )

    flip_faces = pf.nodes.geo.flip_faces(curve_to)

    extrude_1 = pf.nodes.geo.extrude_mesh(
        mesh=flip_faces,
        offset_scale=dimensions.x,
        individual=False,
    )

    set_material_1 = pf.nodes.geo.set_material(
        geometry=extrude_1.mesh,
        material=frame_material,
        selection=True,
    )

    join_geometries = pf.nodes.func.switch(
        switch=join_geometries_1_switch, b=set_material_1
    )
    join = pf.nodes.geo.join_geometry([set_material, join_geometries])

    set_shade_smooth = pf.nodes.geo.set_shade_smooth(geometry=join, shade_smooth=False)

    transform = pf.nodes.geo.transform(
        geometry=set_shade_smooth,
        rotation=(1.5708, 0.0, 1.5708),
        translation=(0, 0, 0),
        scale=(1, 1, 1),
    )
    return transform


def mirror_surface_material_rand(
    rng: pf.RNG,
    vector: t.SocketOrVal[pf.Vector],
) -> pf.Material:
    return mirror_material_rand(rng, vector)


def wall_art_rand(
    rng: pf.RNG,
    dimensions: pf.Vector | None = None,
    panel_material: pf.Material | None = None,
    frame_material: pf.Material | None = None,
) -> WallArtResult:
    r_dims, r_frame_w, r_panel_d, r_frame_mat, r_panel_choice, r_panel_mat = rng.spawn(
        6
    )
    if dimensions is None:
        height = pf.random.log_uniform(r_dims, 0.4, 2.0)
        aspect = pf.random.uniform(r_dims, 0.7, 2.0)
        depth = pf.random.uniform(r_dims, 0.02, 0.05)
        width = height / aspect
        dimensions = pf.Vector((depth, width, height))

    frame_width = pf.random.uniform(r_frame_w, 0.02, 0.1)
    panel_depth_pct = pf.random.uniform(r_panel_d, 0.2, 1.2)

    if frame_material is None:
        vec = pf.nodes.shader.geometry().position
        frame_material = furniture_material_rand(r_frame_mat, vec)

    if panel_material is None:
        vec = pf.nodes.shader.coord().uv
        panel_material_fn = pf.control.choice(
            r_panel_choice,
            [
                (art_pattern_material_rand, 1000),
                (terrazzo.terrazzo_rand, 0.5),
            ],
        )
        panel_material = panel_material_fn(r_panel_mat, vec)

    geo = art_frame(
        dimensions=dimensions,
        frame_width=frame_width,
        frame_material=frame_material,
        panel_material=panel_material,
        panel_depth_pct=panel_depth_pct,
    )
    obj = pf.nodes.to_mesh_object(geo)
    pf.ops.attr.write_attribute(obj, 1.0, "crease_edge", domain="EDGE")
    pf.ops.modifier.subdivide_surface(obj, levels=4, _skip_apply=True)
    return WallArtResult(mesh=obj)


def mirror_rand(rng: pf.RNG, dimensions: pf.Vector | None = None) -> WallArtResult:
    r_mat, r_art = rng.spawn(2)
    vec = pf.nodes.shader.geometry().position
    material = mirror_surface_material_rand(r_mat, vec)
    return wall_art_rand(
        r_art,
        dimensions=dimensions,
        panel_material=material,
    )
